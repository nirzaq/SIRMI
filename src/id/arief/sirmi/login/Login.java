public class Login {
    String kode_user;
    String kode_karyawan;
    String username;
    String password;
    String kategori_user;

    public String getUsername() {
        return username;
    }

    public void setUsername(String username) {
        this.username = username;
    }

    public String getPassword() {
        return password;
    }

    public void setPassword(String password) {
        this.password = password;
    }

    public String getKode_user() {
        return kode_user;
    }

    public void setKode_user(String kode_user) {
        this.kode_user = kode_user;
    }

    public String getKode_karyawan() {
        return kode_karyawan;
    }

    public void setKode_karyawan(String kode_karyawan) {
        this.kode_karyawan = kode_karyawan;
    }

    public String getKategori_user() {
        return kategori_user;
    }

    public void setKategori_user(String kategori_user) {
        this.kategori_user = kategori_user;
    }
    
    


}
