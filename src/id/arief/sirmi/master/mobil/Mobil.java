package id.arief.sirmi.master.mobil;

/**
 *
 * @author arief-asus
 */
public class Mobil {
    String kode_mobil;
    String nama_mobil;
    String merk_mobil;
    String nopol_mobil;
    String jenis_mobil;
    String tahun_pembuatan;
    
    private String pathReport;
    private String resultReport;
    private String sourceReport;

    public String getKode_mobil() {
        return kode_mobil;
    }

    public void setKode_mobil(String kode_mobil) {
        this.kode_mobil = kode_mobil;
    }

    public String getNama_mobil() {
        return nama_mobil;
    }

    public void setNama_mobil(String nama_mobil) {
        this.nama_mobil = nama_mobil;
    }

    public String getMerk_mobil() {
        return merk_mobil;
    }

    public void setMerk_mobil(String merk_mobil) {
        this.merk_mobil = merk_mobil;
    }

    public String getNopol_mobil() {
        return nopol_mobil;
    }

    public void setNopol_mobil(String nopol_mobil) {
        this.nopol_mobil = nopol_mobil;
    }

    public String getJenis_mobil() {
        return jenis_mobil;
    }

    public void setJenis_mobil(String jenis_mobil) {
        this.jenis_mobil = jenis_mobil;
    }

    public String getTahun_pembuatan() {
        return tahun_pembuatan;
    }

    public void setTahun_pembuatan(String tahun_pembuatan) {
        this.tahun_pembuatan = tahun_pembuatan;
    }

    public String getPathReport() {
        return pathReport;
    }

    public void setPathReport(String pathReport) {
        this.pathReport = pathReport;
    }

    public String getResultReport() {
        return resultReport;
    }

    public void setResultReport(String resultReport) {
        this.resultReport = resultReport;
    }

    public String getSourceReport() {
        return sourceReport;
    }

    public void setSourceReport(String sourceReport) {
        this.sourceReport = sourceReport;
    }
    
    
}
