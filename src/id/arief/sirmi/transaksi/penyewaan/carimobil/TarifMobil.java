package id.arief.sirmi.transaksi.penyewaan.carimobil;

/**
 *
 * @author arief-asus
 */
public class TarifMobil {
    String kode_mobil;
    String nama_mobil;
    String merk_mobil;
    String nopol;
    String jenis_mobil;
    String tahun;
    double tarif;
    String jenis;
    String gol_tarif;
    String status;
    int kodeTarif;

    public String getKode_mobil() {
        return kode_mobil;
    }

    public void setKode_mobil(String kode_mobil) {
        this.kode_mobil = kode_mobil;
    }

    public String getNama_mobil() {
        return nama_mobil;
    }

    public void setNama_mobil(String nama_mobil) {
        this.nama_mobil = nama_mobil;
    }

    public String getMerk_mobil() {
        return merk_mobil;
    }

    public void setMerk_mobil(String merk_mobil) {
        this.merk_mobil = merk_mobil;
    }

    public String getNopol() {
        return nopol;
    }

    public void setNopol(String nopol) {
        this.nopol = nopol;
    }

    public String getJenis_mobil() {
        return jenis_mobil;
    }

    public void setJenis_mobil(String jenis_mobil) {
        this.jenis_mobil = jenis_mobil;
    }

    public String getTahun() {
        return tahun;
    }

    public void setTahun(String tahun) {
        this.tahun = tahun;
    }

    public double getTarif() {
        return tarif;
    }

    public void setTarif(double tarif) {
        this.tarif = tarif;
    }

    public String getJenis() {
        return jenis;
    }

    public void setJenis(String jenis) {
        this.jenis = jenis;
    }

    public String getGol_tarif() {
        return gol_tarif;
    }

    public void setGol_tarif(String gol_tarif) {
        this.gol_tarif = gol_tarif;
    }

    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }

    public int getKodeTarif() {
        return kodeTarif;
    }

    public void setKodeTarif(int kodeTarif) {
        this.kodeTarif = kodeTarif;
    }
    
    
}
